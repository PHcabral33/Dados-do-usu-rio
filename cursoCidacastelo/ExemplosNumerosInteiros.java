package cursoCidacastelo;

/**
 * @author Paulo Henrique
 * Programa para exemplificar tipos de dados e variáveis
 * do tipo Inteiro
 */

public class ExemplosNumerosInteiros {
    public static void main (String [] args) {

        // Variáveis do tipo Inteiro: //
        // byte -128 até 127
        byte pontuacao = - 125;
        byte mesesDoAno = 12;

        // short
        // -32768 até 32767
        short inteiroShort = 32767;


        // int
        // -214748364 até 214748367
        int idade = 33;

        // long
        // -9_223_372_036_854_775_808  até  9_223_372_036_854_775_807
        long inteiroLongo = 9_223_372_036_854_775_807L;

        System.out.println(inteiroLongo);


    }
}
