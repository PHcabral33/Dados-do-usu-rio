public class soma {
    public static void main(String [] args){
        System.out.println("Somando valores");

        // Declarando  variáveis
        int a = 30;
        int b = 3;

        // Operações básicas
        int soma = a + b;
        System.out.println("A soma de a + b é "  + soma);


        // Declarando variáis
        int soma1 = 1991;
        int soma2 = 33;

        int somando = soma1 + soma2;
        System.out.println("A soma dos dois números é " + somando);


    }
}
