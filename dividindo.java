public class dividindo {

    public static void main(String [] args){
        System.out.println("Dividindo os valores");

        // Declarando variáveis
        double a = 100;
        double b = 5;

        // Dividindo os valores
        double divide = a / b;

        System.out.println("O resultado da divisão é = " +divide);

        // Declarando variáveis
        double h = 120;
        double t = 20;

        // Dividindo os valores
        double resultado = h / t;

        System.out.println("O resultado da divisão é = " +resultado);





    }
}
